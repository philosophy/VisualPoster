        <div class="desc-wrapper">
            <p>The Second Wave : <b>Silk Screening / Screenprinting</b></p>
            <p>Silk Screening came into its own around the start of
                the 70's.
            </p>
            <p>In 1968, art students from several parisian colleges
                barricaded themselves in, during violent student protests
                and created hundreds of graphics that spewed through the street.
            </p>
            <p>Poster scholar Gene Marie Tempest interviewed several of the
                key participants, and learned that the first poster, Usines,
                Universités, Union (“Factories, Universities, Union”),
                was a lithograph and it took all afternoon to make 30 copies.
            </p>
            <p>This clearly wouldn’t do, and they turned to an
                “American technique”: silkscreening.
            </p>
            <p>Silkscreening didn't require expensive machinery, complicated
                equipment, and an unskilled "helper" could produce 100-200
                posters an hour.
            </p>
            <p>Almost exactly the same scenario played out in America in 1969
                as Harvard campus erupted over conflicts with the campus administration.
                From there, it spread like wildfile - Mexico City, Boston, Berlin, Cuba—
                tiny workshops cranked out untold numbers of posters and street graphics.
            </p>
            <p>Music Promoters were quick to notice the practical effect of these
                posters, and in San Francisco with the birth of the dance concert,
                a rock poster accompanied almost every show that was put on during that era.
                Informal arrangements between the promoters and the artists gave the poster artist
                more liberty than most commercial enterprises would have permitted.
            </p>
            <p>At the time, edgy bands outside the norms were not played on any radio station,
                and the only way you could advertise their shows, was by hanging posters in the streets.
                For some categories of music, this still remains the case today.
            </p>
            <p>Soon, people began pulling the posters off of the telephone poles, almost as quickly as they were put up, and promoters started to give them out at the end of shows to advertise the next week's show.
            </p>
            <span class="block"><b>(Music Poster - Circa 1972)</b></span>
            <?php echo image_asset('lib/70sposter.png', '', array('class' => 'img-desc')); ?>
            <div class="clear"></div>
            <p>By the late 80's however, screenprinting slowly crept into decline, overtaken by other
                mediums, and combined with a reduction in record label budgets, the responsibility
                for procuring creative promotional materials slowly shifted to the musicians themselves.
            </p>
            <p>Limited-edition silk-screened posters declined in favour of low cost prints made on
                Xerox machines in local libraries, or at Copy Shops.
            </p>
            <p>They were glued to buildings and phone poles surreptitiously at night by kids in the scene.
                In this pre-computer era, the flyers were, for lack of a better comparison, "the MySpace of the '80s".
            </p>
        </div>

