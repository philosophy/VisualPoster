        <div class="desc-wrapper">
            <span class="block">The First Wave : <b>Boxing Posters</b>.</span>
            <p>Concert posters date back to the 1940s and ‘50s,
                and were mainly straightforward designs, bold and
                purposely simple, dominated by negative space.
            </p>
            <p>Incorporating simple block lettering with unadorned
                photographs of the performers, they were unchanged
                from the days of the big bands of the 1930's.
            </p>
            <p>Not many of these posters remain, and that's probably the side effect
                of the fact that they pretty much all looked the same and as such,
                weren't always valued for collectability.
            </p>
