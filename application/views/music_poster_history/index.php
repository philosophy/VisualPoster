            <p>The Third Wave : <b>Offset Printing</b></p>
            <p>The main technological breakthrough we now have is that offset printing is the
                cheapest it's ever been, and the previous break-even point of 5,000 prints are is
                now just 500.
            </p>
            <p>More importantly, there is a rich design pool or designers all over the world
                available to craft a masterpiece, often with only a few days notice.
            </p>
            <p>Following a long exodus, street and cafe posters are getting unprecendented
                response rates when good artwork is used and demand from fans for these
                kinds of tokens is apparent.
            </p>
            <p>Many bands and promoters see posters strictly as a promotion tool.
                It is important for that, but it can be so much more, when designed
                with memorabilia in mind.
            </p>
            <p>Music is such a passionate thing for so many people. To have something visually that you like,
                that has a name of a band you like from a show you went to that was a good night—
                it’s a great token of the evening, an an untapped resource for bands balancing
                budget.
            </p>
