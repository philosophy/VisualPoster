        <h3>Anatomy of an A1 Poster</h3>
        <p><span class="highlight">Title - </span>Be sure to pick a title for your deliverable that's memorable and that you're prepared to use consistently.Once you've trained your business friends that it's a wireframe,don't go calling it a gray-screen.</p>
        <p><span class="highlight">Overview - </span>"What problem does this tool solve?"(In a few punchy sentences.)</p>
        <p><span class="highlight">Beauty Shots - </span>Include some pictures of noteworthy examples.Often this will help people realize that they've seen this kind of document before,and oh by the way,it's called a wireframe!</p>
        <p><span class="highlight">Who benefits - </span>This section describes exactly who on the project team will use the material and how.This helps people understand that IAs impact not only the product but also the process.</p>
        <p><span class="highlight">What to Expect - </span>This piece lays out the key steps in the process.Spelling things out in this way makes a new and unfamiliar process feel knowable and doable.This reinforces that working with an IA isn't an aimless exploration for design magic, but a structured approach with a clear end goal.</p>
        <p><span class="highlight">What Next - </span>Now that you've got them interested,give them somewhere to go to learn more.The goal is to get prospctive internal clients to look at past deliverables and begin dreaming about how they can be applied to their own projects.</p>
